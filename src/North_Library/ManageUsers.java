    void ViewUsers()
    {
        
        try {
            conn in = new conn();
            Connection con = in.connect(path, mysql_username, mysql_pass);
            
            Statement stmt = con.createStatement();
            
            try
            {
                stmt.executeUpdate("create table user_info(username varchar(50), password varchar(50), rented int, book1 varchar(50), book2 varchar(50))");
                System.out.println("User_info created.");
            }
            catch(Exception e)
            {
            }
            
            ResultSet rs = stmt.executeQuery("select username, book1, book2 from user_info;");
            
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
            if(jTable1.getRowCount()>0)
            jTable1.setVisible(true);
            else
            JOptionPane.showMessageDialog(this, "No users found.");
            
            con.close();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        } 
    }
