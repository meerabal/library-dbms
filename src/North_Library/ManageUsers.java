    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTable1.isRowSelected(row)==false)
        {
            JOptionPane.showMessageDialog(this, "Please select a user.");
        }

        else
        {
            String str = (String) jTable1.getValueAt(row, 0);

            try {
                conn in = new conn();
                Connection con = in.connect(path, mysql_username, mysql_pass);
            
                int x = JOptionPane.showConfirmDialog(this, "Confirm delete? Data once deleted cannot be recovered.");
            
                if(x==0)
                {
                    Statement stmt = con.createStatement();
                    stmt.executeUpdate("delete from user_info where username='"+str+"'");
                    JOptionPane.showMessageDialog(this, "User deleted successfully.");
                }
                
                if(x==1)
                {
                    
                    JOptionPane.showMessageDialog(this, "Deletion cancelled.");
                }
                            
                
                con.close();
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }

            ManageUsers obj = new ManageUsers();
            obj.setVisible(true);
            dispose();
            obj.setSettings(path, mysql_username, mysql_pass);
            obj.ViewUsers();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

